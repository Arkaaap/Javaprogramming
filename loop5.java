// MULTIPLICATION TABLE 
import java.util.*;
class loop5{
    public static void main (String [] args){
        Scanner sc = new Scanner (System.in);
        System.out.print ("ENTER THE N:");
        int n = sc.nextInt();
        for (int i=1;i<10;i++){
            
            System.out.println  (i*n );
        }
    }
}